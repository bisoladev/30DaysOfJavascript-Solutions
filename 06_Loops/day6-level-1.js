//Exercise 1
//for-loop
for (i = 0; i < 11; i++) {
    console.log(i);
}

//while loop
let u = 0
while (u <= 10 ) {
  console.log(u)
  u++
}

//do - while loop
let x = 0
do {
  console.log(x)
  x++
} while (x <= 10)

